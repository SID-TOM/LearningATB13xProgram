package Test_19June;

public class VariableDeclaration {
    public static void main(String[] args) {
        int VDInteger = 20;
        double VDDouble = 3.14;
        boolean VDbool = true;
        char VDcharacter = 'A';
        System.out.println("Integer:" + VDInteger);
        System.out.println("Double:" + VDDouble);
        System.out.println("Boolean:" + VDbool);
        System.out.println("Char:" + VDcharacter);
    }
}
